namespace eBags.PileOfBeans
{
    /// <summary>
    /// This is a dynamic color enum.
    /// The lower the number the more prevalent it is
    /// Meaning it will win over colors that follow it if they have equal values
    /// </summary>
    public enum Color
    {
        Gray=0,
        Red=1,
        Green=2,
        Blue=3,
        Orange=4,
        Violet=5
    }
}
